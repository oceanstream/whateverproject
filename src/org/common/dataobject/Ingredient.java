package org.common.dataobject;

/**
 * 原材料
 * @author Yone
 *
 */
public class Ingredient {

	//原材料名称
	private String name;
	
	private float count;
	
	
	
	public float getCount() {
		return count;
	}
	
	public void setCount(float count) {
		this.count = count;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
